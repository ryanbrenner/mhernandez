function yearSort($data) {
  $h = '';
  $years = array();
  foreach ($data as $d) {
    $years[] = $d['year'];
  }
  $years = array_unique($years);
  foreach ($years as $y) {
    $sort[$y] = array();
  }
  foreach ($data as $d) {
    $sort[$d['year']][] = $d['exhibition'];
  }
  foreach (array_reverse($sort, true) as $index => $year) {
    $count = 1;
    $h .= '<p class="year">' . $index . '</p><p class="exhibitions">';
    foreach ($year as $y) {
      $h .= $y;
      if ($count != count($year)) {
        $h .= '<br />';
      }
      $count++;
    }
    $h .= '</p>';
  }
  return $h;
}

